<script>
    function showEditOptions() {
      document.getElementById("editoption").style.visibility = "visible";
      document.getElementById("editcontrol").style.visibility = "hidden";
      document.getElementById("closeditcontrol").style.visibility = "visible";
    }
    function hideEditOptions() {
      document.getElementById("editoption").style.visibility = "hidden";
      document.getElementById("editcontrol").style.visibility ="visible";
      document.getElementById("closeditcontrol").style.visibility = "hidden";
    }
</script>